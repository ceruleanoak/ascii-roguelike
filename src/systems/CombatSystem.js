  // Redirect a projectile off a deflector triangle by cell occupancy. Mirrors
  // BoulderSystem's lookup so the bounce uses the same elbow table. Returns
  // true if redirected. Tracks `_lastDeflectorObj` so the projectile doesn't
  // re-deflect on the same tile every frame.
  _tryDeflectorRedirect(proj) {
    const bs = this.game?.boulderSystem;
    if (!bs?.findDeflectorAt) return false;
    // proj.position is the cell top-left; the deflector test wants the bullet's
    // visual center so the triangle interior test lines up with the rendered glyph.
    const cs = GRID.CELL_SIZE;
    const deflector = bs.findDeflectorAt(proj.position.x + cs / 2, proj.position.y + cs / 2);
    if (!deflector) {
      if (proj._lastDeflectorObj) proj._lastDeflectorObj = null;
      return false;
    }
    if (proj._lastDeflectorObj === deflector) return false;
    const out = bs.deflectVelocity(deflector.data.deflectorElbow, proj.velocity.vx, proj.velocity.vy);
    if (!out) return false;
    // Update velocity in place — no snap. The bullet continues from its actual
    // hit point inside the triangle and exits naturally on the next frames.
    proj.velocity.vx = out.vx;
    proj.velocity.vy = out.vy;
    if (proj.drawAngle != null) proj.drawAngle = Math.atan2(out.vy, out.vx);
    proj._lastDeflectorObj = deflector;
    proj.reflected = true;
    this.audioSystem?.playSFX?.('ricochet', 0.5);
    return true;
  }

  reflectBullet(bullet, obj) {
    // Calculate reflection angle with some randomness
    const angle = Math.atan2(bullet.velocity.vy, bullet.velocity.vx);
    const reflectedAngle = angle + Math.PI + (Math.random() - 0.5) * 1.0;

    const speed = Math.sqrt(bullet.velocity.vx ** 2 + bullet.velocity.vy ** 2);
    bullet.velocity.vx = Math.cos(reflectedAngle) * speed;
    bullet.velocity.vy = Math.sin(reflectedAngle) * speed;
    if (bullet.drawAngle != null) bullet.drawAngle = reflectedAngle;

    // Mark as reflected so it doesn't hit player
    bullet.reflected = true;

    // Placeholder ricochet SFX — wired via main.js; silently no-ops if unloaded.
    this.audioSystem?.playSFX?.('ricochet', 0.6);
  }

  // Acid Blade: flood-fill connected water tiles, permanently converting the
  // entire pond to acid (modelled as the existing 'poisoned' waterState with
  // Infinity duration). 4-connected adjacency on the cell grid.
  // (Electrify is NOT instant like this — shock-on-water routes through
  // ElectricitySystem so the charge spreads tile-by-tile at a fixed rate.)
  _acidFloodFillWater(startObj, backgroundObjects) {
    const CELL = GRID.CELL_SIZE;
    const key = (x, y) => `${Math.round(x / CELL)},${Math.round(y / CELL)}`;

    const waterMap = new Map();
    for (const obj of backgroundObjects) {
      if (obj.destroyed) continue;
      if (!obj.isWater || !obj.isWater()) continue;
      waterMap.set(key(obj.position.x, obj.position.y), obj);
    }

    const visited = new Set();
    const queue = [startObj];
    visited.add(key(startObj.position.x, startObj.position.y));

    while (queue.length > 0) {
      const obj = queue.shift();
      obj.setWaterState('poisoned', Infinity);

      const cx = Math.round(obj.position.x / CELL);
      const cy = Math.round(obj.position.y / CELL);
      const neighborKeys = [
        `${cx - 1},${cy}`, `${cx + 1},${cy}`,
        `${cx},${cy - 1}`, `${cx},${cy + 1}`
      ];
      for (const nk of neighborKeys) {
        if (visited.has(nk)) continue;
        const neighbor = waterMap.get(nk);
        if (!neighbor) continue;
        visited.add(nk);
        queue.push(neighbor);
      }
    }
  }

  conductElectricity(sourceObj, damage, enemies, player = null) {
    return conductElectricityImpl(this, sourceObj, damage, enemies, player);
  }
